function flip_Image (myImage: Image) {
    cloneimage = myImage.clone()
    cloneimage.flipX()
    return cloneimage
}
function filpanamionup (list: Image[]) {
    anamion = list
    for (let value of anamion) {
        value.flipX()
    }
    return anamion
}
function Put_tilemap_on_tilemap (col: number, row: number, num: number) {
    for (let y = 0; y <= 13; y++) {
        for (let x = 0; x <= 4; x++) {
            tiles.setTileAt(tiles.getTileLocation(y + col, x + row), tileUtil.getTileImage(random_gen01[num], tiles.getTileLocation(y, x)))
        }
    }
}
